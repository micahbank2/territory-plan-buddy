import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are a sharp, concise sales assistant embedded in a territory planner app. The user manages a pipeline of B2B prospects.

You have access to their current prospect data (provided as JSON context). Each prospect has:
- name: company name
- stage: pipeline stage (Not Started, Actively Prospecting, Meeting Booked, Closed Won, Closed Lost)
- score: prospect score (higher = better fit)
- tier: priority tier (Tier 1-4)
- priority: Hot, Warm, Cold, or Dead
- industry: vertical
- locations: number of locations
- lastTouched: last interaction date
- daysSinceTouch: days since last interaction
- status: Prospect or Churned
- competitor: incumbent competitor if known

Rules:
- Be concise and actionable. No fluff.
- Use bullet points and bold for key info.
- When asked about specific prospects, reference them by name.
- If asked to prioritize, consider score, tier, days since touch, and priority.
- Format numbers cleanly. Use relative dates ("2 weeks ago") when helpful.
- If you don't have enough data to answer, say so briefly.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, prospectContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemContent = prospectContext
      ? `${SYSTEM_PROMPT}\n\nCurrent prospect data:\n${prospectContext}`
      : SYSTEM_PROMPT;

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: systemContent },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit reached — try again in a minute." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits exhausted — add credits in Settings → Workspace → Usage." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
